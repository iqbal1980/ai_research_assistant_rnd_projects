class CharData:
    def __init__(self, text: str, seq_len: int, device: torch.device, train_frac: float = 0.9) -> None:
        if len(text) < seq_len + 2:
            repeats = math.ceil((seq_len + 2) / max(1, len(text))) + 1
            text = text * repeats
        chars = sorted(set(text))
        self.stoi = {ch: i for i, ch in enumerate(chars)}
        self.itos = {i: ch for ch, i in self.stoi.items()}
        self.vocab_size = len(chars)
        encoded = torch.tensor([self.stoi[ch] for ch in text], dtype=torch.long)
        split = max(seq_len + 2, int(len(encoded) * train_frac))
        split = min(split, len(encoded) - seq_len - 2)
        self.train = encoded[:split].to(device)
        self.val = encoded[split:].to(device)
        if self.val.numel() < seq_len + 2:
            self.val = encoded[-(seq_len + 2):].to(device)
        self.seq_len = seq_len
        self.device = device

    def get_batch(self, split: str, batch_size: int) -> Tuple[torch.Tensor, torch.Tensor]:
        data = self.train if split == "train" else self.val
        max_start = data.numel() - self.seq_len - 1
        if max_start <= 0:
            raise ValueError("Dataset too small for the requested sequence length")
        ix = torch.randint(0, max_start, (batch_size,), device=self.device)
        x = torch.stack([data[i:i + self.seq_len] for i in ix])
        y = torch.stack([data[i + 1:i + 1 + self.seq_len] for i in ix])
        return x, y


def load_embedded_corpus() -> str:
    try:
        raw = base64.b64decode("".join(EMBEDDED_CORPUS_ZLIB_B64.split()))
        return zlib.decompress(raw).decode("utf-8", errors="replace")
    except Exception:
        return FALLBACK_TEXT


def load_text(path: Optional[str]) -> Tuple[str, str]:
    if path:
        with open(path, "r", encoding="utf-8", errors="replace") as f:
            txt = f.read()
        return txt, path
    return load_embedded_corpus(), "embedded_original_corpus"


