def _pad_to_multiple(x: torch.Tensor, multiple: int, dim: int = 2) -> Tuple[torch.Tensor, int]:
    orig = x.shape[dim]
    pad_len = (multiple - (orig % multiple)) % multiple
    if pad_len == 0:
        return x, orig
    pad_shape = list(x.shape)
    pad_shape[dim] = pad_len
    pad = torch.zeros(*pad_shape, dtype=x.dtype, device=x.device)
    return torch.cat([x, pad], dim=dim), orig


def normalized_kernel_attention_weights(scores: torch.Tensor, mask: torch.Tensor, kernel: str) -> torch.Tensor:
    """Return normalized attention weights over the last dim.

    The kernel receives max-centered logits x <= 0 and produces positive weights.
    This is for experimental GP-discovered kernels, not a fused production kernel.
    """
    neg = torch.finfo(scores.dtype).min
    masked_scores = scores.masked_fill(~mask, neg)
    if kernel == "softmax":
        return torch.softmax(masked_scores, dim=-1)

    # Center like stable softmax: x = score - max(score). Valid x values are <= 0.
    row_max = masked_scores.max(dim=-1, keepdim=True).values
    x = (scores - row_max).float()
    x = torch.where(mask, x, torch.zeros_like(x))

    if kernel == "rational":
        # GP-discovered / hand-simplified candidate: g(x)=1/(1-x)^2
        g = 1.0 / torch.square(1.0 - x)
    elif kernel == "clipquad":
        # GP-discovered candidate: g(x)=max(1+0.25x,0.125)^2
        g = torch.square(torch.clamp(1.0 + 0.25 * x, min=0.125))
    elif kernel == "polymax":
        # Best poly-only GP candidate: max(1.5+x, max(1+0.25x,0.125)^2)
        g = torch.maximum(1.5 + x, torch.square(torch.clamp(1.0 + 0.25 * x, min=0.125)))
    elif kernel == "linear_clip":
        # Simple cheap baseline from the Pareto front: g(x)=max(1+0.5x,0.125)
        g = torch.clamp(1.0 + 0.5 * x, min=0.125)
    else:
        raise ValueError(f"Unknown attention kernel: {kernel}")

    g = torch.where(mask, g, torch.zeros_like(g))
    denom = g.sum(dim=-1, keepdim=True).clamp_min(1e-12)
    return (g / denom).to(scores.dtype)


def proxy_block_cage_attention(
    q: torch.Tensor,
    k: torch.Tensor,
    v: torch.Tensor,
    *,
    query_block_size: int,
    key_block_size: int,
    block_cage_size: int,
    causal: bool = True,
    attention_kernel: str = "softmax",
) -> torch.Tensor:
    """q/k/v: [B,H,T,D]. Select key blocks with pooled scores, then local normalized attention."""
    B, H, Tq, D = q.shape
    Tk = k.shape[2]
    q_pad, orig_tq = _pad_to_multiple(q, query_block_size, dim=2)
    k_pad, orig_tk = _pad_to_multiple(k, key_block_size, dim=2)
    v_pad, _ = _pad_to_multiple(v, key_block_size, dim=2)

    Tq_pad = q_pad.shape[2]
    Tk_pad = k_pad.shape[2]
    Nq = Tq_pad // query_block_size
    Nk = Tk_pad // key_block_size
    m = min(block_cage_size, Nk)
    scale = 1.0 / math.sqrt(D)

    q_blocks = q_pad.view(B, H, Nq, query_block_size, D)
    k_blocks = k_pad.view(B, H, Nk, key_block_size, D)
    v_blocks = v_pad.view(B, H, Nk, key_block_size, D)

    with torch.no_grad():
        # Pool in float32 for selection stability. This selection is intentionally discrete.
        q_pool = q_blocks.float().mean(dim=3)  # [B,H,Nq,D]
        k_pool = k_blocks.float().mean(dim=3)  # [B,H,Nk,D]
        block_scores = torch.matmul(q_pool, k_pool.transpose(-1, -2)) * scale  # [B,H,Nq,Nk]

        key_block_starts = torch.arange(Nk, device=q.device).view(1, 1, 1, Nk) * key_block_size
        query_block_ends = (torch.arange(Nq, device=q.device).view(1, 1, Nq, 1) + 1) * query_block_size - 1
        valid_key_block = key_block_starts < orig_tk
        if causal:
            valid_block = valid_key_block & (key_block_starts <= query_block_ends)
        else:
            valid_block = valid_key_block.expand(1, 1, Nq, Nk)
        block_scores = block_scores.masked_fill(~valid_block, float("-inf"))
        topk_idx = torch.topk(block_scores, k=m, dim=-1).indices  # [B,H,Nq,m]

    gather_idx = topk_idx[..., None, None].expand(B, H, Nq, m, key_block_size, D)
    expanded_k_blocks = k_blocks.unsqueeze(2).expand(B, H, Nq, Nk, key_block_size, D)
    expanded_v_blocks = v_blocks.unsqueeze(2).expand(B, H, Nq, Nk, key_block_size, D)
    k_sel = torch.gather(expanded_k_blocks, dim=3, index=gather_idx).reshape(B, H, Nq, m * key_block_size, D)
    v_sel = torch.gather(expanded_v_blocks, dim=3, index=gather_idx).reshape(B, H, Nq, m * key_block_size, D)

    scores = torch.einsum("bhnqd,bhnkd->bhnqk", q_blocks, k_sel) * scale

    q_pos = torch.arange(Tq_pad, device=q.device).view(Nq, query_block_size)
    key_offsets = torch.arange(key_block_size, device=q.device)
    key_pos = (topk_idx[..., None] * key_block_size + key_offsets).reshape(B, H, Nq, m * key_block_size)

    valid_query = q_pos < orig_tq
    valid_key = key_pos < orig_tk
    if causal:
        token_mask = valid_key[..., None, :] & (key_pos[..., None, :] <= q_pos.view(1, 1, Nq, query_block_size, 1))
    else:
        token_mask = valid_key[..., None, :].expand(B, H, Nq, query_block_size, m * key_block_size)
    token_mask = token_mask & valid_query.view(1, 1, Nq, query_block_size, 1)

    probs = normalized_kernel_attention_weights(scores, token_mask, attention_kernel)
    out_blocks = torch.einsum("bhnqk,bhnkd->bhnqd", probs, v_sel)
    out = out_blocks.reshape(B, H, Tq_pad, D)[:, :, :orig_tq, :]
    return out


# ----------------------------- model -----------------------------


class CausalSelfAttention(nn.Module):
    def __init__(self, model_dim: int, heads: int, method: str, query_block_size: int, key_block_size: int, block_cage_size: int, attention_kernel: str = "softmax") -> None:
        super().__init__()
        if model_dim % heads != 0:
            raise ValueError("model_dim must be divisible by heads")
        self.model_dim = model_dim
        self.heads = heads
        self.head_dim = model_dim // heads
        self.method = method
        self.query_block_size = query_block_size
        self.key_block_size = key_block_size
        self.block_cage_size = block_cage_size
        self.attention_kernel = attention_kernel
        self.qkv = nn.Linear(model_dim, 3 * model_dim, bias=False)
        self.out = nn.Linear(model_dim, model_dim, bias=False)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        B, T, C = x.shape
        qkv = self.qkv(x).view(B, T, 3, self.heads, self.head_dim)
        q, k, v = qkv.unbind(dim=2)
        q = q.transpose(1, 2).contiguous()
        k = k.transpose(1, 2).contiguous()
        v = v.transpose(1, 2).contiguous()
        if self.method == "dense":
            y = F.scaled_dot_product_attention(q, k, v, is_causal=True)
        elif self.method == "proxy_topk":
            y = proxy_block_cage_attention(
                q, k, v,
                query_block_size=self.query_block_size,
                key_block_size=self.key_block_size,
                block_cage_size=self.block_cage_size,
                causal=True,
                attention_kernel=self.attention_kernel,
            )
        else:
            raise ValueError(f"Unknown attention method: {self.method}")
        y = y.transpose(1, 2).contiguous().view(B, T, C)
        return self.out(y)


class TransformerBlock(nn.Module):
    def __init__(self, model_dim: int, heads: int, mlp_mult: int, method: str, query_block_size: int, key_block_size: int, block_cage_size: int, attention_kernel: str = "softmax") -> None:
        super().__init__()
        self.ln1 = nn.LayerNorm(model_dim)
        self.attn = CausalSelfAttention(model_dim, heads, method, query_block_size, key_block_size, block_cage_size, attention_kernel)
        self.ln2 = nn.LayerNorm(model_dim)
        hidden = model_dim * mlp_mult
        self.mlp = nn.Sequential(
            nn.Linear(model_dim, hidden, bias=False),
            nn.GELU(),
            nn.Linear(hidden, model_dim, bias=False),
        )

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        x = x + self.attn(self.ln1(x))
        x = x + self.mlp(self.ln2(x))
        return x


class TinyTransformerLM(nn.Module):
    def __init__(
        self,
        vocab_size: int,
        seq_len: int,
        model_dim: int,
        heads: int,
        layers: int,
        mlp_mult: int,
        method: str,
        query_block_size: int,
        key_block_size: int,
        block_cage_size: int,
        attention_kernel: str = "softmax",
    ) -> None:
        super().__init__()
        self.vocab_size = vocab_size
        self.seq_len = seq_len
        self.token_emb = nn.Embedding(vocab_size, model_dim)
        self.pos_emb = nn.Embedding(seq_len, model_dim)
        self.blocks = nn.ModuleList([
            TransformerBlock(model_dim, heads, mlp_mult, method, query_block_size, key_block_size, block_cage_size, attention_kernel)
            for _ in range(layers)
        ])
        self.ln_f = nn.LayerNorm(model_dim)
        self.lm_head = nn.Linear(model_dim, vocab_size, bias=False)
        # Tied embeddings when dimensions match by construction.
        self.lm_head.weight = self.token_emb.weight

    def forward(self, idx: torch.Tensor, last_only: bool = False) -> torch.Tensor:
        B, T = idx.shape
        if T > self.seq_len:
            raise ValueError(f"Input length {T} exceeds model seq_len {self.seq_len}")
        pos = torch.arange(T, device=idx.device)
        x = self.token_emb(idx) + self.pos_emb(pos)[None, :, :]
        for block in self.blocks:
            x = block(x)
        x = self.ln_f(x)
        if last_only:
            x = x[:, -1:, :]
        return self.lm_head(x)


# ----------------------------- training/eval -----------------------------


@torch.no_grad()
def estimate_loss(model: TinyTransformerLM, data: CharData, split: str, batch_size: int, eval_iters: int, device: torch.device, dtype: torch.dtype) -> float:
    model.eval()
    losses: List[float] = []
    for _ in range(eval_iters):
        x, y = data.get_batch(split, batch_size)
        with autocast_context(device, dtype):
            logits = model(x, last_only=False)
            loss = F.cross_entropy(logits.reshape(-1, logits.size(-1)).float(), y.reshape(-1))
        losses.append(float(loss.item()))
    return float(statistics.mean(losses))


@torch.no_grad()
def logit_agreement(
    dense: TinyTransformerLM,
    method_model: TinyTransformerLM,
    data: CharData,
    batch_size: int,
    eval_iters: int,
    device: torch.device,
    dtype: torch.dtype,
) -> Dict[str, float]:
    dense.eval()
    method_model.eval()
    mse_vals: List[float] = []
    mae_vals: List[float] = []
    top1_vals: List[float] = []
    top5_vals: List[float] = []
    kl_vals: List[float] = []
    for _ in range(eval_iters):
        x, _ = data.get_batch("val", batch_size)
        with autocast_context(device, dtype):
            dlog = dense(x, last_only=True).float().squeeze(1)
            mlog = method_model(x, last_only=True).float().squeeze(1)
        mse_vals.append(float(F.mse_loss(mlog, dlog).item()))
        mae_vals.append(float((mlog - dlog).abs().mean().item()))
        d_top1 = dlog.argmax(dim=-1)
        m_top1 = mlog.argmax(dim=-1)
        top1_vals.append(float((d_top1 == m_top1).float().mean().item()))
        d_top5 = torch.topk(dlog, k=min(5, dlog.size(-1)), dim=-1).indices
        top5_vals.append(float((m_top1[:, None] == d_top5).any(dim=-1).float().mean().item()))
        d_lp = F.log_softmax(dlog, dim=-1)
        m_lp = F.log_softmax(mlog, dim=-1)
        d_p = d_lp.exp()
        kl_vals.append(float(F.kl_div(m_lp, d_p, reduction="batchmean", log_target=False).item()))
    return {
        "last_logit_mse_vs_dense": float(statistics.mean(mse_vals)),
        "last_logit_mae_vs_dense": float(statistics.mean(mae_vals)),
        "last_top1_agreement": float(statistics.mean(top1_vals)),
        "last_top5_containment": float(statistics.mean(top5_vals)),
        "last_kl_dense_to_method": float(statistics.mean(kl_vals)),
    }


def train_dense(
    model: TinyTransformerLM,
    data: CharData,
    args: argparse.Namespace,
    device: torch.device,
    dtype: torch.dtype,
) -> List[Dict[str, float]]:
    model.train()
    opt = torch.optim.AdamW(model.parameters(), lr=args.learning_rate, weight_decay=args.weight_decay)
    history: List[Dict[str, float]] = []

    scaler = None
    # Use scaler only for CUDA fp16. bf16 generally does not need it.
    if device.type == "cuda" and dtype == torch.float16:
        try:
            scaler = torch.amp.GradScaler("cuda")
        except Exception:
            scaler = torch.cuda.amp.GradScaler()

    t0 = time.perf_counter()
    for step in range(1, args.train_steps + 1):
        x, y = data.get_batch("train", args.batch)
        opt.zero_grad(set_to_none=True)
        with autocast_context(device, dtype):
            logits = model(x, last_only=False)
            loss = F.cross_entropy(logits.reshape(-1, logits.size(-1)).float(), y.reshape(-1))
        if scaler is not None:
            scaler.scale(loss).backward()
            scaler.unscale_(opt)
            torch.nn.utils.clip_grad_norm_(model.parameters(), args.grad_clip)
            scaler.step(opt)
            scaler.update()
        else:
            loss.backward()
            torch.nn.utils.clip_grad_norm_(model.parameters(), args.grad_clip)
            opt.step()

        if step == 1 or step % args.log_every == 0 or step == args.train_steps:
            cuda_sync(device)
            elapsed = time.perf_counter() - t0
            tok_per_s = step * args.batch * args.seq_len / max(elapsed, 1e-9)
            record = {"step": step, "train_loss": float(loss.item()), "tokens_per_second_train": float(tok_per_s)}
            history.append(record)
            print(f"step {step:5d}/{args.train_steps}: train_loss={loss.item():.4f}, train_tok/s={tok_per_s:.0f}")
    return history


@torch.no_grad()
def measure_prefill_last(
    model: TinyTransformerLM,
    data: CharData,
    batch_size: int,
    warmups: int,
    repeats: int,
    device: torch.device,
    dtype: torch.dtype,
) -> Dict[str, float]:
    model.eval()
    x, _ = data.get_batch("val", batch_size)
    for _ in range(warmups):
        with autocast_context(device, dtype):
            _ = model(x, last_only=True)
    cuda_sync(device)
    reset_peak_memory(device)
    times: List[float] = []
    for _ in range(repeats):
        cuda_sync(device)
        start = time.perf_counter()
        with autocast_context(device, dtype):
            _ = model(x, last_only=True)
        cuda_sync(device)
        times.append((time.perf_counter() - start) * 1000.0)
    return {
        "prefill_last_ms_median": float(statistics.median(times)),
        "prefill_last_ms_mean": float(statistics.mean(times)),
        "prefill_last_ms_p90": float(percentile(times, 0.90)),
        "peak_cuda_mem_mb": peak_memory_mb(device),
    }


def make_model(args: argparse.Namespace, vocab_size: int, method: str, block_cage_size: int, device: torch.device, attention_kernel: str = "softmax") -> TinyTransformerLM:
    model = TinyTransformerLM(
        vocab_size=vocab_size,
        seq_len=args.seq_len,
        model_dim=args.model_dim,
        heads=args.heads,
        layers=args.layers,
        mlp_mult=args.mlp_mult,
        method=method,
        query_block_size=args.query_block_size,
        key_block_size=args.key_block_size,
        block_cage_size=block_cage_size,
        attention_kernel=attention_kernel,
    ).to(device)
    return model


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--device", default="cuda")
    parser.add_argument("--dtype", default="fp16", choices=["fp16", "bf16", "fp32"])
    parser.add_argument("--text-file", default=None, help="UTF-8 text file. If omitted, uses the embedded original corpus from the article. No vocab/token files are needed.")
    parser.add_argument("--write-embedded-corpus", default=None, help="Optional path to write the embedded corpus and exit.")
    parser.add_argument("--seq-len", type=int, default=512)
    parser.add_argument("--batch", type=int, default=8)
    parser.add_argument("--model-dim", type=int, default=256)
    parser.add_argument("--heads", type=int, default=4)
    parser.add_argument("--layers", type=int, default=2)
    parser.add_argument("--mlp-mult", type=int, default=4)
    parser.add_argument("--query-block-size", type=int, default=32)
    parser.add_argument("--key-block-size", type=int, default=32)
    parser.add_argument("--block-cage-sizes", type=int, nargs="+", default=[1, 2, 4, 8])
    parser.add_argument("--kernels", nargs="+", default=["softmax", "rational", "clipquad", "polymax"],
                        choices=["softmax", "rational", "clipquad", "polymax", "linear_clip"],
                        help="Local attention normalization kernel for proxy_topk.")
    parser.add_argument("--train-steps", type=int, default=300)
    parser.add_argument("--eval-iters", type=int, default=20)
    parser.add_argument("--learning-rate", type=float, default=3e-4)
    parser.add_argument("--weight-decay", type=float, default=0.1)
    parser.add_argument("--grad-clip", type=float, default=1.0)
    parser.add_argument("--log-every", type=int, default=50)
    parser.add_argument("--warmups", type=int, default=1)
    parser.add_argument("--repeats", type=int, default=5)
    parser.add_argument("--seed", type=int, default=1234)
    parser.add_argument("--output-dir", default="cage_text_quality")
    parser.add_argument("--save-model", action="store_true", help="Save trained dense model checkpoint.")
    args = parser.parse_args()

    if args.write_embedded_corpus:
        corpus = load_embedded_corpus()
        with open(args.write_embedded_corpus, "w", encoding="utf-8") as f:
            f.write(corpus)
        print(f"Wrote embedded corpus to: {os.path.abspath(args.write_embedded_corpus)}")
        print(f"Characters: {len(corpus)}")
        return

    if args.device == "cuda" and not torch.cuda.is_available():
        print("CUDA requested but not available; falling back to CPU.")
        args.device = "cpu"
    device = torch.device(args.device)
    dtype = dtype_from_name(args.dtype)
    seed_everything(args.seed)
    os.makedirs(args.output_dir, exist_ok=True)

    text, source = load_text(args.text_file)
    data = CharData(text, args.seq_len, device)

    config = vars(args).copy()
    config.update({
        "text_source": source,
        "text_chars": len(text),
        "vocab_size": data.vocab_size,
        "selected_token_capacities": [args.key_block_size * c for c in args.block_cage_sizes],
    })

    print("Environment:")
    print(json.dumps(env_info(device, args.dtype), indent=2))
    print("\nConfig:")
    print(json.dumps(config, indent=2))
    print("\nTraining dense teacher/model...")

    dense = make_model(args, data.vocab_size, method="dense", block_cage_size=1, device=device)
    train_history = train_dense(dense, data, args, device, dtype)

    print("\nEvaluating dense baseline...")
    dense_val_loss = estimate_loss(dense, data, "val", args.batch, args.eval_iters, device, dtype)
    dense_latency = measure_prefill_last(dense, data, args.batch, args.warmups, args.repeats, device, dtype)
    rows: List[Dict[str, object]] = []
    dense_row: Dict[str, object] = {
        "method": "dense",
        "attention_kernel": "softmax",
        "block_cage_size": None,
        "selected_token_capacity": None,
        "density_vs_dense": 1.0,
        "val_loss": dense_val_loss,
        "loss_delta_vs_dense": 0.0,
        "last_logit_mse_vs_dense": 0.0,
        "last_logit_mae_vs_dense": 0.0,
        "last_top1_agreement": 1.0,
        "last_top5_containment": 1.0,
        "last_kl_dense_to_method": 0.0,
        **dense_latency,
    }
    rows.append(dense_row)
    print(f"dense: val_loss={dense_val_loss:.4f}, prefill_last={dense_latency['prefill_last_ms_median']:.3f} ms")

    dense_state = {k: v.detach().cpu() for k, v in dense.state_dict().items()}

    for cage in args.block_cage_sizes:
        capacity = args.key_block_size * cage
        for kernel in args.kernels:
            print(f"\nEvaluating proxy_topk cage={cage}, capacity={capacity}, kernel={kernel} ...")
            proxy = make_model(args, data.vocab_size, method="proxy_topk", block_cage_size=cage, device=device, attention_kernel=kernel)
            proxy.load_state_dict(dense_state, strict=True)
            proxy_val_loss = estimate_loss(proxy, data, "val", args.batch, args.eval_iters, device, dtype)
            agreement = logit_agreement(dense, proxy, data, args.batch, args.eval_iters, device, dtype)
            latency = measure_prefill_last(proxy, data, args.batch, args.warmups, args.repeats, device, dtype)
            row: Dict[str, object] = {
                "method": f"proxy_topk_{kernel}",
                "attention_kernel": kernel,
                "block_cage_size": cage,
                "selected_token_capacity": capacity,
                "density_vs_dense": capacity / args.seq_len,
                "val_loss": proxy_val_loss,
                "loss_delta_vs_dense": proxy_val_loss - dense_val_loss,
                **agreement,
                **latency,
            }
            rows.append(row)
            speedup = dense_latency["prefill_last_ms_median"] / max(latency["prefill_last_ms_median"], 1e-9)
            print(
                f"proxy cage={cage}, kernel={kernel}: val_loss={proxy_val_loss:.4f}, "
                f"delta={proxy_val_loss - dense_val_loss:+.4f}, "
                f"prefill_last={latency['prefill_last_ms_median']:.3f} ms, "
                f"speedup_vs_dense={speedup:.2f}x, top1={agreement['last_top1_agreement']:.3f}, "
                f"top5={agreement['last_top5_containment']:.3f}"
            )
            del proxy
            if device.type == "cuda":
                torch.cuda.empty_cache()

    csv_path = os.path.join(args.output_dir, "cage_gp_kernel_quality.csv")
    json_path = os.path.join(args.output_dir, "cage_gp_kernel_quality.json")
    fieldnames = sorted(set().union(*(r.keys() for r in rows)))
    with open(csv_path, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        for r in rows:
            writer.writerow(r)
    with open(json_path, "w", encoding="utf-8") as f:
        json.dump({
            "environment": env_info(device, args.dtype),
            "config": config,
            "train_history": train_history,
            "rows": rows,
            "interpretation": {
                "val_loss": "All-token validation cross-entropy on character-level next-token prediction.",
                "loss_delta_vs_dense": "Proxy loss minus trained dense loss; closer to zero is better.",
                "prefill_last_ms_median": "Generation-style prefill: process full context, return LM logits only for final token.",
                "top1_agreement": "How often proxy and dense choose the same final-token argmax.",
                "warning": "This is a tiny char-level model. The GP kernels are not fused; timing is prototype evidence, not production-kernel evidence.",
                "kernels": "softmax is standard local softmax; rational=1/(1-x)^2; clipquad=max(1+0.25x,0.125)^2; polymax=max(1.5+x, max(1+0.25x,0.125)^2).",
            },
        }, f, indent=2)

    if args.save_model:
        ckpt_path = os.path.join(args.output_dir, "dense_char_lm.pt")
        torch.save({
            "model_state_dict": dense.state_dict(),
            "config": config,
            "stoi": data.stoi,
            "itos": data.itos,
        }, ckpt_path)
        print(f"Saved dense checkpoint: {os.path.abspath(ckpt_path)}")

    print("\nResults:")
    display_fields = [
        "method", "attention_kernel", "block_cage_size", "selected_token_capacity", "density_vs_dense", "val_loss",
        "loss_delta_vs_dense", "prefill_last_ms_median", "last_logit_mse_vs_dense",
        "last_top1_agreement", "last_top5_containment", "peak_cuda_mem_mb",
    ]
    widths = {k: max(len(k), *(len(f"{r.get(k, ''):.5g}") if isinstance(r.get(k), float) else len(str(r.get(k, ''))) for r in rows)) for k in display_fields}
    print("  ".join(k.ljust(widths[k]) for k in display_fields))
    print("  ".join("-" * widths[k] for k in display_fields))
